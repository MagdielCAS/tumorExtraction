% --- Executes on button press in pushbutton9.
function pushbutton9_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton9 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

    %Botão de processar
    imagem = handles.ImgData;
    [ImagemTumor, ImagemSegmentada, features] = processar(imagem);
    axes(handles.axes3);
    imshow(ImagemTumor,[]);
    axes(handles.axes4);
    %[Contraste 1, Homogeneidade 2, Entropia 3, Energia 4,Media 5,Desvio_padrao 6, RMS 7,Variancia 8,Suavidade 9,Achatamento 10,Assimetria 11,IDM 12]
    set(handles.edit5,'string',features(5));%Media
    set(handles.edit6,'string',features(6));%Desivio Padrao
    set(handles.edit7,'string',features(3));%Entropia
    set(handles.edit8,'string',features(7));%RMS
    set(handles.edit9,'string',features(8));%Variancia
    set(handles.edit10,'string',features(9));%Suavidade
    set(handles.edit11,'string',features(10));%Achatamento
    set(handles.edit12,'string',features(11));%Assimetria
    set(handles.edit13,'string',features(12));%IDM
    set(handles.edit14,'string',features(1));%Contraste
    set(handles.edit15,'string',features(4));%Energia
    set(handles.edit16,'string',features(2));%Homogeneidade
    imshow(ImagemSegmentada,[]);
    if ~isfield(handles,'RedeNeural')
        warndlg('Treine a rede para classificar!');
    else
        net = handles.RedeNeural;
        verif=net(reshape(features,12,1));
        if verif > 0
            set(handles.text22,'string','Benigno.');
        else
            set(handles.text22,'string','Maligno.');
        end
    end



